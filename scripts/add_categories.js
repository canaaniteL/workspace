const inputFile = process.argv[2] || path.join(__dirname, '..', '..', 'Users', 'canaanzhu', 'Downloads', '面试', '算法', '算法.txt');
const outputFile = process.argv[3] || inputFile;

let content = fs.readFileSync(inputFile, 'utf-8');

// 匹配每个题目块的标题行，在 **核心思路** 行之前插入 **分类** 行
// 格式: ## 【LeetCode 数字】题名
const lines = content.split('\n');
const newLines = [];

for (let i = 0; i < lines.length; i++) {
  newLines.push(lines[i]);
  
  // 检查是否是题目标题行
  const titleMatch = lines[i].match(/^##\s+【LeetCode\s+(\d+)】/);
  if (titleMatch) {
    const num = titleMatch[1];
    const cat = categoryMap[num];
    if (cat) {
      // 查找后续行，在 **核心思路** 行之前插入分类
      // 先跳过当前标题行之后的空行和题目描述行
      // 我们在紧接着标题行之后插入
      // 找到下一个非空行
      let j = i + 1;
      // 检查是否已经有 **分类** 行
      let hasCat = false;
      for (let k = j; k < Math.min(j + 5, lines.length); k++) {
        if (lines[k].match(/^\*\*分类\*\*/)) {
          hasCat = true;
          break;
        }
      }
      if (!hasCat) {
        // 在标题行之后紧跟插入分类行
        newLines.push(`**分类**：${cat}`);
      }
    }
  }
}

fs.writeFileSync(outputFile, newLines.join('\n'), 'utf-8');
